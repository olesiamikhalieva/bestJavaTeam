package hw_09_02_18.MenuCoffee.drinks;

public class DefaultDrink {

    private String name;
    private int price;

    public DefaultDrink() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
