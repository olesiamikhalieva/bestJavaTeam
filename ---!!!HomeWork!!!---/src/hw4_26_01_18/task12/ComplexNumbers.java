package hw4_26_01_18.task12;

public class ComplexNumbers {

    private double realD;
    private double imagineD;

    public ComplexNumbers() {
    }

    public ComplexNumbers(double realD, double imagineD) {
        this.realD = realD;
        this.imagineD = imagineD;
    }


    public double getRealD() {
        return realD;
    }

    public void setRealD(double realD) {
        this.realD = realD;
    }

    public double getImagineD() {
        return imagineD;
    }

    public void setImagineD(double imagineD) {
        this.imagineD = imagineD;
    }

}
